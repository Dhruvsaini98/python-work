countries = ['india', 'argentina', 'zimbabwe', 'west indies', 'australia', 'new zealand', 'pakistan']
print(countries)

#sorted() function
sorted_countries = sorted(countries)
print(sorted_countries)
print(countries)
sorted_countrie_reversealpha = sorted(countries, reverse=True)
print(sorted_countrie_reversealpha)
print(countries)

#reverse() method
countries.reverse()
print(countries)
countries.reverse()
print(countries)

#sort() method
countries.sort()
print(countries)
countries.sort(reverse=True)
print(countries)